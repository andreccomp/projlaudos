package br.academico.classes;

public class Curso {

	
	private int idCurso;
	
	private String nmCurso;
	
	private int qtPeriodo;

	/**
	 * @return the idCurso
	 */
	public int getIdCurso() {
		return idCurso;
	}

	/**
	 * @param idCurso the idCurso to set
	 */
	public void setIdCurso(int idCurso) {
		this.idCurso = idCurso;
	}

	/**
	 * @return the nmCurso
	 */
	public String getNmCurso() {
		return nmCurso;
	}

	/**
	 * @param nmCurso the nmCurso to set
	 */
	public void setNmCurso(String nmCurso) {
		this.nmCurso = nmCurso;
	}

	/**
	 * @return the qtPeriodo
	 */
	public int getQtPeriodo() {
		return qtPeriodo;
	}

	/**
	 * @param qtPeriodo the qtPeriodo to set
	 */
	public void setQtPeriodo(int qtPeriodo) {
		this.qtPeriodo = qtPeriodo;
	}

	
	
}
